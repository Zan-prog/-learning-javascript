// Arithmetic operators
let a = 10;
let b = 5;

console.log("Addition", a + b);
console.log("Subtraction", a - b);
console.log("Multiplication", a * b);
console.log("Division", a / b);

// Comparison operators
console.log("a > b:", a > b);
console.log("a < b:", a < b);
console.log("a === b:", a === b);
console.log("a !== b:", a !== b);

// Logical operators
let isStudent = true
let hasTime = false

console.log("isStudent AND hasTime:", isStudent && hasTime);
console.log("isStudent OR hasTime:", isStudent || hasTime);
console.log("NOT isStudent:", !isStudent);
