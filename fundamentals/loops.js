// for loop
console.log("For loop example:");

for (let i = 1; i <= 5; i++) {
  console.log("Number:", i);
}

// while loop
console.log("While loop example:");

let count = 1;

while (count <= 5) {
  console.log("Count:", count);
  count++;
}

// do...while loop
console.log("Do...while loop example:");

let x = 1;

do {
  console.log("Value:", x);
  x++;
} while (x <= 3);
