let age = 16;

// Simple if / else
if (age >= 18) {
  console.log("You are an adult.");
} else {
  console.log("You are under 18.");
}

// if / else if / else
let grade = 7;

if (grade >= 9) {
  console.log("Excellent!");
} else if (grade >= 6) {
  console.log("Approved.");
} else {
  console.log("Failed.");
}

// Using boolean variables
let isStudent = true;

if (isStudent) {
  console.log("The user is a student.");
} else {
  console.log("The user is not a student.");
}
